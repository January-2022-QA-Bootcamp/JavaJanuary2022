package com.enthrall.catchup;

import java.math.BigDecimal;

public class DefaultValues {

	public static int a;
	public static long b;
	public static double c;
	public static float d;
	public static byte f;
	public static short s;
	public static char e;
	public static boolean g;
	
	public static String string;
	public static BigDecimal bigDecimal;
	
	public static void main(String[] args) {
		System.out.println("Default value of int is :" + a);
		System.out.println("Default value of long is :" + b);
		System.out.println("Default value of double is :" + c);
		System.out.println("Default value of float is :" + d);
		System.out.println("Default value of byte is :" + f);
		System.out.println("Default value of char is :" + e);
		System.out.println("Default value of boolean is :" + g);
		System.out.println("Default value of short is :" + s);
		System.out.println("Default value of String is :" + string);
		System.out.println("Default value of BigDecimal is :" + bigDecimal);
	}
}
